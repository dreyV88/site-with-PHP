                <p>
                    Quelques mots sur notre programme 2014 <br>
                    Nous proposons en 2014 un repas dansant le 27 avril et la reconduction de la Saint Sylvestre. <br>
                    Un spectacle « sons et lumières » à Cagnoncles ? Pourquoi pas ! <br><br>

                    Il faut se lancer pour voir. <br>
                    Cet événement autour de l'histoire du village est en préparation. <br>
                    Il serait programmé en septembre 2014 ou 2015 en fonction de l'avancement des travaux de deuxième phase et de la fin des répétitions
                    de mise au point du spectacle. <br>
                    Après l'écriture et le cadencement d'une première approche la plus exhaustive possible a propos des dialogues, des saynètes, des
                    musiques, des chansons, des projections et des jeux de lumières, commencera, dès le début du mois de décembre, une longue phase
                    de prospection afin: <br>
                </p>
                <ul >
                    <li>de trouver des personnes compétentes dans le domaine « sons et lumières » pour nous aider,</li>
                    <li>de rechercher des figurants, des comédiens, afin de définir, concevoir et confectionner ou louer les costumes,</li>
                    <li>de mettre en scène,</li>
                    <li>de cerner et de se procurer le matériel nécessaire .</li>
                </ul>
                <p>
                    Et, finalement, de trouver la meilleure formule pour que le spectacle soit le plus conforme possible à nos rêves malgré les réalités
                    économiques et techniques qui ne vont pas manquer d'entraver certains effets souhaités. <br>
                    Nous envisageons en 2015 ou 2016, la rénovation et la décoration de l'intérieur de l'Eglise après la troisième phase de travaux.
                    Nous nous efforcerons de retrouver, en partie, la décoration existante en début du XX° siècle
                    Par exemple, l'ancien chemin de croix est en phase finale de restauration au niveau des toiles. <br>
                    Les cadres seront nettoyés, réparés. Un nouveau cadre a été entièrement refait. <br><br>
                    A noter que suite à l'exposition inter- associative d'octobre 2012 pilotée par l'ASPEC (Association de Sauvegarde du Patrimoine et de
                    l'Environnement du Cambrésis), un comité inter-associations pour la mémoire du Cambrésis a été créé. <br>
                    Notre association en tant que membre de ce comité participera du 18 au 24 octobre 2014 à une exposition consacrée au centenaire de la guerre 1914 - 1918. <br>
